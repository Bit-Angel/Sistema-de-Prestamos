    private void jMenuItem14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem14ActionPerformed
      String identidad = JOptionPane.showInputDialog("Ingresa el telefono del prestatario ");
      int aux=0;
      long tel = Long.parseLong(identidad);
      for(Cliente i:prestatarios){
            if(i.telefono==tel){
               JOptionPane.showMessageDialog(null, "Busqueda exitosa");
               interfaz.setText("Datos del prestatario: \nNombre: "+i.getNombre()+" "+i.getApellido()+" "+i.getApellido2()+
                        "\nID: "+i.getIdentidad()+"\nCorreo: "+i.getCorreo()+"\nTelefono: "+i.getTelefono()+
                        "\nDatos de prestamo: \n"+i.getPrestamo()); 
               aux=1;
            }
      }
      if(aux==0)JOptionPane.showMessageDialog(null, "No se encontró conicidencias.");
    }//GEN-LAST:event_jMenuItem14ActionPerformed

