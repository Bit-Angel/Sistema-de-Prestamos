    public void capturaDatos(int a){
        String nombre = JOptionPane.showInputDialog("Ingresa tu nombre: ");
        String apellido = JOptionPane.showInputDialog("Ingresa tu primer apellido: ");
        String apellido2 = JOptionPane.showInputDialog("Ingresa tu segundo apellido: ");
        JOptionPane.showMessageDialog(null, "Bienvenido "+nombre+" "+apellido+" "+apellido2);
        String identidad = JOptionPane.showInputDialog("Ingresa tu numero de identidad: ");
        int id = Integer.parseInt(identidad);
        String telefono = JOptionPane.showInputDialog("Ingresa tu numero de telefono: ");
        long tel = Long.parseLong(telefono);
        String correo = JOptionPane.showInputDialog("Ingresa tu correo: ");
        Cliente cliente = new Cliente(id,nombre,apellido,apellido2,correo,tel);
        
        prestatarios.add(cliente);
        numPrestamo=1;
        interfaz.setText("Datos capturados correctamente "+nombre);   
        int resp = JOptionPane.showConfirmDialog(null,"¿Desea solicitar un prestamo?");
        if (JOptionPane.OK_OPTION == resp){
          capturaPrestamo();
        }
        else{
          JOptionPane.showMessageDialog(null,"Bienvenido "+nombre+" "+apellido);
        }
    }
    public void  capturaPrestamo(){
      String identidad = JOptionPane.showInputDialog("¿Cual es tu numero de indentificacion? ");
      int id = Integer.parseInt(identidad);
      for(Cliente i:prestatarios){
            if(i.identidad==id){
                String valor = JOptionPane.showInputDialog("Ingresa el valor del prestamo: ");
                Double cantidad = Double.parseDouble(valor);
                if(cantidad<=0){
                    JOptionPane.showMessageDialog(null,"La cantidad del prestamo debe ser mayor a 0 ");
                    continue;
                }
                boolean veri = i.compruebaSaldo(cantidad);
                if(veri==false){
                     JOptionPane.showMessageDialog(null,"La cantidad del prestamo excede el monto maximo 10,000. ");
                    continue;
                }
                String[] diasSemana = {"3","4","5","6"};
                Icon icono = new ImageIcon(getClass().getResource("pngtree-calendar-icon-png-image_1521539.jpg"));
                String cuotas = (String) JOptionPane.showInputDialog(null, "Numero de cuotas", "Cuotas", JOptionPane.DEFAULT_OPTION, icono, diasSemana, diasSemana[3]);
                int meses = Integer.parseInt(cuotas);
                boolean verifica = comprobarAutorizacion();
                if(verifica){
                    Fecha auto = new Fecha(global.dd,global.mm,global.aa);
                    Fecha entrega = new Fecha(global.dd,global.mm,global.aa);
                    entrega.obtenerFechaEntrega();
                    Prestamo presta = new Prestamo(numPrestamo,cantidad,auto,entrega,meses);
                    if(meses==3){
                        presta.tresCuotas();
                    }
                    if(meses==4){
                        presta.cuatroCuotas();
                    }
                    if(meses==5){
                        presta.cincoCuotas();
                    }
                    if(meses==6){
                        presta.seisCuotas();
                    }
                    i.prestamo.add(presta);
                    numPrestamo++;
                    
                }
                interfaz.setText("Datos capturados: \nNombre: "+i.getNombre()+" "+i.getApellido()+" "+i.getApellido2()+
                        "\nID: "+i.getIdentidad()+"\nCorreo: "+i.getCorreo()+"\nTelefono: "+i.getTelefono()+
                        "\nDatos de prestamo: \n"+i.getPrestamo());
                Reporteprestamos=Reporteprestamos+"Prestamo #"+num+"\n";
                Reporteprestamos=Reporteprestamos+i.getPrestamo();
                archivo=archivo+"\n\n\n"+interfaz.getText();
                
                num++;
            }
            
            
        }
    }
